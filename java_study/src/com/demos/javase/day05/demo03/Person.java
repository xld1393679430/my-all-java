package com.demos.javase.day05.demo03;

public class Person {
    // 私有
    private void fn1() {
        System.out.println("private fn1");
    }

    // 默认 default
    void fn2() {
        System.out.println("private fn2");
    }

    // 保护
    protected void fn3() {
        System.out.println("private fn3");
    }

    // 公开
    public void fn4() {
        System.out.println("private fn4");
    }
}
